#include<stdio.h>

int main() {
    float scoer = 90.5;
    printf("Score: %.1f\n", scoer);

    int score = 90.5;
    printf("Score: %d\n", score);

    return 0;
}
// 1.ผลที่ได้ต่างกันเนื่องจาก ตัวเเปร float ใช้เก็บเเละเเสดงเลขทศนิยม score = 90.5 - ตัวเเปร int ไม่สามารถเก็บเลขทศนิยมได้ score = 90
// 2.เพื่อให้สามารถเลือกใช้ตัวแปรในการเก็บเเละเเสดงผลได้ตามรูปเเบบข้อมูล
// 3.จากการทดลองพบว่า float ใช้เก็บข้อมูลตัวเลขที่มีทศนิยม int ใช้เก็บข้อมูลตัวเลขที่ไม่มีทศนิยมนะ