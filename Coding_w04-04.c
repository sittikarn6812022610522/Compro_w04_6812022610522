#include<stdio.h>

int main() {
    int score= 90.5;
    float scoree = 90.5;
    printf("Score: %d\n", score);
    printf("Score: %.1f\n",scoree);

    return 0;
}

// 1.ผลลัพธ์ที่ได้ต่างกันอย่างไร?  int จะไม่เก็บทศนิยม แต่ float จะเก็บทศนิยม
// 2.ทำไมต้องเลือก Data Type ให้ถูกต้อง? เพื่อแสดงค่าที่ถูกต้องและแม่นยำ
// 3.สรุป:int จะเหมาะกับข้อมูลตัวเลขที่ไม่เก็บทศนิยม  float จะเหมาะกับข้อมูลตัวเลขที่เก็บทศนิยม
